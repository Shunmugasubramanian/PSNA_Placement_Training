import java.util.*;
public class Solution {
    public static void main(String[] args) {        
        Scanner s = new Scanner(System.in);
        int n1 = s.nextInt();
        int n2 = s.nextInt();
        int i, sum = 0;
        //Multiplication without '*' operator
        for(i = 1; i <= n2; i++){
                sum += n1;
        }
        System.out.println(sum);
        s.close();
    }
}
